package org.greenscape.weblet.webcontent.rest;

public class WebContentDisplayParam {
	private String title;
	private String content;
	private String siteId;
	private String pageletId;
	private String articleId;
	private String displayId;

	/**
	 * @return the title
	 */
	public String getTitle() {
		return title;
	}

	/**
	 * @param title
	 *            the title to set
	 */
	public void setTitle(String title) {
		this.title = title;
	}

	/**
	 * @return the content
	 */
	public String getContent() {
		return content;
	}

	/**
	 * @param content
	 *            the content to set
	 */
	public void setContent(String content) {
		this.content = content;
	}

	/**
	 * @return the siteId
	 */
	public String getSiteId() {
		return siteId;
	}

	/**
	 * @param siteId
	 *            the siteId to set
	 */
	public void setSiteId(String siteId) {
		this.siteId = siteId;
	}

	/**
	 * @return the pageletId
	 */
	public String getPageletId() {
		return pageletId;
	}

	/**
	 * @param pageletId
	 *            the pageletId to set
	 */
	public void setPageletId(String pageletId) {
		this.pageletId = pageletId;
	}

	/**
	 * @return the articleId
	 */
	public String getArticleId() {
		return articleId;
	}

	/**
	 * @param articleId
	 *            the articleId to set
	 */
	public void setArticleId(String articleId) {
		this.articleId = articleId;
	}

	/**
	 * @return the displayId
	 */
	public String getDisplayId() {
		return displayId;
	}

	/**
	 * @param displayId
	 *            the displayId to set
	 */
	public void setDisplayId(String displayId) {
		this.displayId = displayId;
	}
}
